#!/usr/bin/env python3
"""
Read experiment_results.json and write results.tex with LaTeX macro definitions.
Run after run_baseline_experiments.py and/or run_custom_experiments.py finish.
Usage: python3 generate_latex_results.py
"""

import json, os

BASE_DIR  = os.path.dirname(os.path.abspath(__file__))
JSON_PATH = os.path.join(BASE_DIR, 'experiment_results.json')
TEX_PATH  = os.path.join(BASE_DIR, 'results.tex')


def fmt(mean, std):
    """Format as mean[std] with sensible rounding."""
    if mean is None:
        return r'\textit{TBD}'
    m = round(mean, 1)
    s = round(std, 1) if std is not None else 0.0
    if m >= 100:
        m = int(round(mean))
        s = int(round(std)) if std is not None else 0
    return f'{m}[{s}]'


def redef(name, value):
    return f'\\renewcommand{{\\{name}}}{{{value}}}\n'


with open(JSON_PATH) as f:
    results = json.load(f)

lines = ['% Auto-generated by generate_latex_results.py — do not edit manually\n']

# --- Stop-and-Go bandwidth sweep ---
sng_mapping = {
    'sng_200k': ('SngGoodputTwoHundredK', 'SngOverheadTwoHundredK'),
    'sng_20k':  ('SngGoodputTwentyK',     'SngOverheadTwentyK'),
    'sng_2k':   ('SngGoodputTwoK',        'SngOverheadTwoK'),
}
for key, (g_macro, o_macro) in sng_mapping.items():
    if key in results:
        r = results[key]
        g_val = fmt(r.get('goodput_mean'), r.get('goodput_std'))
        o_val = fmt(r.get('overhead_mean'), r.get('overhead_std'))
    else:
        g_val = o_val = r'\textit{TBD}'
    lines.append(redef(g_macro, g_val))
    lines.append(redef(o_macro, o_val))

# --- Custom window-size sweep ---
window_sizes = [1, 5, 10, 20, 40]
labels = ['One', 'Five', 'Ten', 'Twenty', 'Forty']
for w, lbl in zip(window_sizes, labels):
    key = f'custom_w{w}'
    if key in results:
        r = results[key]
        g_val = fmt(r.get('goodput_mean'), r.get('goodput_std'))
        o_val = fmt(r.get('overhead_mean'), r.get('overhead_std'))
    else:
        g_val = o_val = r'\textit{TBD}'
    lines.append(redef(f'CustGoodputW{lbl}', g_val))
    lines.append(redef(f'CustOverheadW{lbl}', o_val))

# --- Custom vs SNG comparison (best window at 200k) ---
if 'custom_200k' in results:
    r = results['custom_200k']
    lines.append(redef('CustGoodputBest', fmt(r.get('goodput_mean'), r.get('goodput_std'))))
    lines.append(redef('CustOverheadBest', fmt(r.get('overhead_mean'), r.get('overhead_std'))))

# --- Sensitivity ---
sens_tags = [('5pct', 'FivePct'), ('2pct', 'TwoPct'), ('0pct', 'ZeroPct')]
for tag, lbl in sens_tags:
    for proto, prefix in [('custom', 'Cust'), ('sng', 'Sng')]:
        key = f'{proto}_sens_{tag}'
        if key in results:
            r = results[key]
            g_val = fmt(r.get('goodput_mean'), r.get('goodput_std'))
            o_val = fmt(r.get('overhead_mean'), r.get('overhead_std'))
        else:
            g_val = o_val = r'\textit{TBD}'
        lines.append(redef(f'{prefix}SensGoodput{lbl}', g_val))
        lines.append(redef(f'{prefix}SensOverhead{lbl}', o_val))

with open(TEX_PATH, 'w') as f:
    f.writelines(lines)

print(f'Written {TEX_PATH}')
for line in lines[1:]:
    print(' ', line.rstrip())
